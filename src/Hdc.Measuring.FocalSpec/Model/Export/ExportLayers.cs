namespace Hdc.Measuring
{
    /// <summary>
    /// Layer of a point cloud.
    /// </summary>
    public enum ExportLayer
    {
        /// <summary>
        /// Full data.
        /// </summary>
        All,

        /// <summary>
        /// Layer with min. distance to the sensor.
        /// </summary>
        Top,

        /// <summary>
        /// Layer with max. distance to the sensor.
        /// </summary>
        Bottom,

        /// <summary>
        /// Layer with max intensity.
        /// </summary>
        Brightest
    }
}